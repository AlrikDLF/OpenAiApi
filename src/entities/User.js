class User {
    constructor(id, username, email, firstname, lastname, createdAt, updatedAt) {
      this.id = id;
      this.username = username;
      this.email = email;
      this.firstname = firstname;
      this.lastname = lastname;
      this.createdAt = createdAt;
      this.updatedAt = updatedAt;
    }
  }
  
  export default User;
  