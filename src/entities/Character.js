class Character {
    constructor(id, name, description, universe_id, createdAt, updatedAt) {
      this.id = id;
      this.name = name;
      this.description = description;
      this.universe_id = universe_id;
      this.createdAt = createdAt;
      this.updatedAt = updatedAt;
    }
  }
  
  module.exports = Character;
  